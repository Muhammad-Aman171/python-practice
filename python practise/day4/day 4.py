

# Prime Factors: 

userInput = int(input("enter your number : "))

primeFactorArr =  []

for i in range(2 , userInput + 1):
    while userInput % i == 0 :
        primeFactorArr.append(i)
        userInput = userInput // i
    if userInput == 1 : 
        break
print("prime factors of this number is ",primeFactorArr)





